  en: { name: 'English', code: 'en', flag: '🇺🇸' },
  zh: { name: '中文 (Chinese)', code: 'zh', flag: '🇨🇳' },
  ja: { name: '日本語 (Japanese)', code: 'ja', flag: '🇯🇵' },
